class Solution {
  public:
    bool canPair(vector<int> nums, int k) {
        // Code here.
        unordered_map<int,int> mp;
        if(nums.size()%2!=0){
            return false;
        }
        for(int i=0;i<nums.size();i++){
            mp[nums[i]%k]++;
        }
        if(mp[0]%2==1){
            return false;
        }
       unordered_map<int,int> :: iterator it;
        for(it=mp.begin();it!=mp.end();it++){
            if(it->second!=mp[(k-it->first)%k]){
                    return false;
                }
        }
        return true;
    }
};


class Solution{
  public:
    vector <int> countDistinct (int arr[], int n, int k)
    {
        //code here.
        vector<int> v;
        map<int,int> mp;
        for(int i=0;i<k;i++){
            mp[arr[i]]++;
        }
        v.push_back(mp.size());
        for(int i=0;i<n-k;i++){
            
           
            mp[arr[i+k]]+=1;
            if(mp[arr[i]]==1)
            mp.erase(arr[i]);
            else
            mp[arr[i]]-=1;
            v.push_back(mp.size());
             
        }
        return v;
    }
};

